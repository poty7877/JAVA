package ch02;

public class LongExam {
	
	public static void main(String[] args) {
		// long 타입은 int 타입의 두배인 64bit 값을 갖는다.
		// 대부분 금융, 은행, 증권, 등 돈에 관련된 변수타입으로 사용된다.
		// 초기값 입력시 int와 차이를 주기위해, 숫자 뒤에 l, L을 붙인다.
		
		long var1 = 10 ; // int범위 안에서는 L 생략 가능.
		long var2 = 20L ;
		long var3 = 2222222222L ; // int 범위 초과 시 L은 필수다.
		
		
		
	}

}
